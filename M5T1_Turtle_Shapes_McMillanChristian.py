t.left(47)
t.forward(47)
t.left(90)             
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(180)
t.forward(100)
t.left(154)
t.forward(30)

# stop the turtle
win.mainloop()            
